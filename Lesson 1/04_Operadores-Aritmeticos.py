# Operadores Aritméticos
suma = 4 + 7
print(suma)

resta = 4 - 1
print(resta)

multiplicacion = 4 * 4
print(multiplicacion)

division = 10 / 3  # devuelve el cociente y/o resultado con decimales de la división
print(division)

resto_division = 10 % 3
print(resto_division)  # devuelve el resto de la división

division_total = 17 // 3
print(division_total)  # devuelve el cociente y/o resultado entero sin decimales de la división

exponente = 5 ** 3
print(exponente)  # se le conoce como exponente y/o potencia

# Concatenación +
texto1 = " hola"
texto2 = " martin"
print(texto1 + texto2)

# Repetir el texto * n
triple = texto1 * 3
print(triple)
